package kr.or.ddit.vo;

public class ChattingVO {
	private String chat_no;
	private String mem_id;
	private String cr_no;
	private String mem_nick;
	private String char_content;
	private String char_read;
	public String getChat_no() {
		return chat_no;
	}
	public void setChat_no(String chat_no) {
		this.chat_no = chat_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getCr_no() {
		return cr_no;
	}
	public void setCr_no(String cr_no) {
		this.cr_no = cr_no;
	}
	public String getMem_nick() {
		return mem_nick;
	}
	public void setMem_nick(String mem_nick) {
		this.mem_nick = mem_nick;
	}
	public String getChar_content() {
		return char_content;
	}
	public void setChar_content(String char_content) {
		this.char_content = char_content;
	}
	public String getChar_read() {
		return char_read;
	}
	public void setChar_read(String char_read) {
		this.char_read = char_read;
	}
	
}
